#Vediamo i diversi tipi di variabili in Python

#Int
integerNumber = 5
print("Numero intero:", integerNumber)

#String
string = "Sono una stringa"
print("Stringa:", string)

#Float
floatNumber = 5.8
print("Numero float:", floatNumber)

#Complex (numeri complessi)
complexNumber = 5j + 2
print("Numero complesso:", complexNumber)

#Bool (valore booleano, cioè può essere vero o falso)
boolValue = True
print("Valore booleano:", boolValue)